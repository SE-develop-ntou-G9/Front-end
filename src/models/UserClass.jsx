export default class UserClass {
  constructor(userName, password, phone, carType, licenseNum, email) {
    this.userName = userName;            // 名字
    this.password = password;    // 密碼
    this.phone = phone;          // 電話
    this.carType = carType;      // 車型
    this.licenseNum = licenseNum;// 車牌
    this.email = email;          // 電子郵件
  }
}